package com.ftc.wechat.product.pojo;

public class WechatProductSpec {
	private Integer specId;
	
	private String specTitle;
	
	private String specName;
	
	private String specProductid;

	public Integer getSpecId() {
		return specId;
	}

	public void setSpecId(Integer specId) {
		this.specId = specId;
	}

	public String getSpecTitle() {
		return specTitle;
	}

	public void setSpecTitle(String specTitle) {
		this.specTitle = specTitle;
	}

	public String getSpecName() {
		return specName;
	}

	public void setSpecName(String specName) {
		this.specName = specName;
	}

	public String getSpecProductid() {
		return specProductid;
	}

	public void setSpecProductid(String specProductid) {
		this.specProductid = specProductid;
	}
}